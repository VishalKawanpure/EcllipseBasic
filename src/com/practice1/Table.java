package com.practice1;

public class Table {
	
	
	public static void main(String[] args) {
		
		
		int num=20;
		
		for(int i=2; i<=num; i++) {
			
			System.out.println("Table of  "+ i);
			
			for(int j=1; j<=10; j++) {
				
				System.out.println(j*i);
				
			}
			
			System.out.println("=======================");
		}
		
		
	}

}
